const mixtureRegExp = new RegExp(`([${charRange.greek}][${charRange.latin}])|([${charRange.latin}][${charRange.greek}])`)

const ltgFactory = function (map) {
  return function (string) {
    return string.split("").map(a => map[a] || a).join("")
  };
};

